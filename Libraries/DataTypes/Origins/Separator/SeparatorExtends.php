class SeparatorExtends
{
    //--------------------------------------------------------------------------------------------------------
    // Key
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string
    //
    //--------------------------------------------------------------------------------------------------------
    protected static $key = "+-?||?-+" ;

    //--------------------------------------------------------------------------------------------------------
    // Separator
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string
    //
    //--------------------------------------------------------------------------------------------------------
    protected static $separator = "|?-++-?|";

    //--------------------------------------------------------------------------------------------------------
    // Protected Security
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string  $data
    //
    //--------------------------------------------------------------------------------------------------------
    protected static function _security($data)
    {
        return str_replace([self::$key, self::$separator], '', $data);
    }
}
