interface FunctionsInterface
{
    //--------------------------------------------------------------------------------------------------------
    // Call Array
    //--------------------------------------------------------------------------------------------------------
    //
    // @param callable $callback
    // @param array    $params
    //
    //--------------------------------------------------------------------------------------------------------
    public function callArray(Callable $callback, Array $params = []);

    //--------------------------------------------------------------------------------------------------------
    // Call
    //--------------------------------------------------------------------------------------------------------
    //
    // @param variadic $args
    //
    //--------------------------------------------------------------------------------------------------------
    public function call(...$args);

    //--------------------------------------------------------------------------------------------------------
    // Static Call Array
    //--------------------------------------------------------------------------------------------------------
    //
    // @param callable $callback
    // @param array    $params
    //
    //--------------------------------------------------------------------------------------------------------
    public function staticCallArray(Callable $callback, Array $params = []);

    //--------------------------------------------------------------------------------------------------------
    // Static Call
    //--------------------------------------------------------------------------------------------------------
    //
    // @param variadic $args
    //
    //--------------------------------------------------------------------------------------------------------
    public function staticCall(...$args);

    //--------------------------------------------------------------------------------------------------------
    // Shutdown
    //--------------------------------------------------------------------------------------------------------
    //
    // @param variadic $args
    //
    //--------------------------------------------------------------------------------------------------------
    public function shutdown(...$args);

    //--------------------------------------------------------------------------------------------------------
    // Tick
    //--------------------------------------------------------------------------------------------------------
    //
    // @param variadic $args
    //
    //--------------------------------------------------------------------------------------------------------
    public function tick(...$args);

    //--------------------------------------------------------------------------------------------------------
    // Untick
    //--------------------------------------------------------------------------------------------------------
    //
    // @param variadic $args
    //
    //--------------------------------------------------------------------------------------------------------
    public function untick(...$args);

    //--------------------------------------------------------------------------------------------------------
    // Defined
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public function defined() : Array;
}
