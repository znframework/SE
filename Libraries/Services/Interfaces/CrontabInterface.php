interface CrontabInterface
{
    //--------------------------------------------------------------------------------------------------------
    // Queue -> 5.3.6
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  int      $id
    // @param  callable $callable
    // @oaram  int      $decrement = 1
    // @return void
    //
    //--------------------------------------------------------------------------------------------------------
    public function queue(Int $id, Callable $callable, Int $decrement = 1);

    //--------------------------------------------------------------------------------------------------------
    // Limit -> 5.3.6
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  int $id
    // @oaram  int $limit = 1
    // @return void
    //
    //--------------------------------------------------------------------------------------------------------
    public function limit(Int $id, Int $limit = 1);

    //--------------------------------------------------------------------------------------------------------
    // Driver
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $driver: empty
    // @return object
    //
    //--------------------------------------------------------------------------------------------------------
    public function driver(String $driver) : Crontab;

    //--------------------------------------------------------------------------------------------------------
    // List Array
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  void
    // @return array
    //
    //--------------------------------------------------------------------------------------------------------
    public function listArray() : Array;

    //--------------------------------------------------------------------------------------------------------
    // List
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  void
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function list() : String;

    //--------------------------------------------------------------------------------------------------------
    // Remove
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $name: crontab.txt
    // @return object
    //
    //--------------------------------------------------------------------------------------------------------
    public function remove($key = NULL);

    //--------------------------------------------------------------------------------------------------------
    // Run
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $cmd: empty
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function run(String $cmd = NULL);

    //--------------------------------------------------------------------------------------------------------
    // Debug
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  bool   $status: true
    // @return object
    //
    //--------------------------------------------------------------------------------------------------------
    public function debug(Bool $status) : Crontab;

    //--------------------------------------------------------------------------------------------------------
    // Controller
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $file: empty
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function controller(String $file);

    //--------------------------------------------------------------------------------------------------------
    // Controller
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $file: empty
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function command(String $file);

    //--------------------------------------------------------------------------------------------------------
    // Wget
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $file: empty
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function wget(String $url);
}
