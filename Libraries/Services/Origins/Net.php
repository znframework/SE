use ZN\Helpers\Converter;
use ZN\Services\Exception\SocketException;

class Net implements NetInterface
{
    //--------------------------------------------------------------------------------------------------------
    // Check DNS
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    // @param string $type
    //
    //--------------------------------------------------------------------------------------------------------
    public static function checkDns(String $host, String $type = 'MX') : Bool
    {
        return checkdnsrr(self::cleanHttp($host), $type);
    }

    //--------------------------------------------------------------------------------------------------------
    // DNS Records
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    // @param string $type
    // @param bool   $raw
    //
    //--------------------------------------------------------------------------------------------------------
    public static function dnsRecords(String $host, String $type = 'any', Bool $raw = false) : \stdClass
    {
        $dns = dns_get_record(self::cleanHttp($host), Converter::toConstant($type, 'DNS_'), $auth, $add, $raw);

        return (object)
        [
            'records' => $dns,
            'authns'  => $auth,
            'addtl'   => $add
        ];
    }

    //--------------------------------------------------------------------------------------------------------
    // MX Records
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    //
    //--------------------------------------------------------------------------------------------------------
    public static function mxRecords(String $host) : \stdClass
    {
        $mx = getmxrr(self::cleanHttp($host), $mxhosts, $weight);

        return (object)
        [
            'records' => $mxhosts,
            'weight'  => $weight
        ];
    }

    //--------------------------------------------------------------------------------------------------------
    // Socket
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    // @param int    $port
    // @param int    $timeout
    //
    //--------------------------------------------------------------------------------------------------------
    public static function socket(String $host, Int $port = -1, Int $timeout = 60)
    {
        $socket = fsockopen(self::cleanHttp($host), $port, $errno, $errstr, $timeout);

        if( ! empty($errno) )
        {
            throw new SocketException($errno.'-'.$errstr);
        }

        return $socket;
    }

    //--------------------------------------------------------------------------------------------------------
    // Psocket
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    // @param int    $port
    // @param int    $timeout
    //
    //--------------------------------------------------------------------------------------------------------
    public static function psocket(String $host, Int $port = -1, Int $timeout = 60)
    {
        $socket = pfsockopen(self::cleanHttp($host), $port, $errno, $errstr, $timeout);

        if( ! empty($errno) )
        {
            throw new SocketException($errno.'-'.$errstr);
        }

        return $socket;
    }

    //--------------------------------------------------------------------------------------------------------
    // IP v4 To Host
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $ip
    //
    //--------------------------------------------------------------------------------------------------------
    public static function ipv4ToHost(String $ip) : String
    {
        return gethostbyaddr($ip);
    }

    //--------------------------------------------------------------------------------------------------------
    // Host To IP v4
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    //
    //--------------------------------------------------------------------------------------------------------
    public static function hostToIpv4(String $host) : String
    {
        return gethostbyname(self::cleanHttp($host));
    }

    //--------------------------------------------------------------------------------------------------------
    // Host To IP v4 List
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    //
    //--------------------------------------------------------------------------------------------------------
    public static function hostToIpv4List(String $host) : Array
    {
        return gethostbynamel(self::cleanHttp($host));
    }

    //--------------------------------------------------------------------------------------------------------
    // Protocol Number
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $name
    //
    //--------------------------------------------------------------------------------------------------------
    public static function protocolNumber(String $name) : Int
    {
        return getprotobyname($name);
    }

    //--------------------------------------------------------------------------------------------------------
    // Protocol Name
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int $number
    //
    //--------------------------------------------------------------------------------------------------------
    public static function protocolName(Int $number) : String
    {
        return getprotobynumber($number);
    }

    //--------------------------------------------------------------------------------------------------------
    // Service Port
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $service
    // @param string $protocol
    //
    //--------------------------------------------------------------------------------------------------------
    public static function servicePort(String $service, String $protocol) : Int
    {
        return getservbyname($service, $protocol);
    }

    //--------------------------------------------------------------------------------------------------------
    // Service Name
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int    $port
    // @param string $protocol
    //
    //--------------------------------------------------------------------------------------------------------
    public static function serviceName(Int $port, String $protocol) : String
    {
        return getservbyport($port, $protocol);
    }

    //--------------------------------------------------------------------------------------------------------
    // Local
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public static function local() : String
    {
        return gethostname();
    }

    //--------------------------------------------------------------------------------------------------------
    // Rcode
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int $code
    //
    //--------------------------------------------------------------------------------------------------------
    public static function rcode(Int $code = NULL)
    {
        return http_response_code($code);
    }

    //--------------------------------------------------------------------------------------------------------
    // Chr To Ip V4
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $chr
    //
    //--------------------------------------------------------------------------------------------------------
    public static function chrToIpv4(String $chr) : String
    {
        return inet_ntop($chr);
    }

    //--------------------------------------------------------------------------------------------------------
    // Ip v4 To Chr
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $addr
    //
    //--------------------------------------------------------------------------------------------------------
    public static function ipv4ToChr(String $addr) : String
    {
        return inet_pton($addr);
    }

    //--------------------------------------------------------------------------------------------------------
    // Ip v4 To Number
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $ip
    //
    //--------------------------------------------------------------------------------------------------------
    public static function ipv4ToNumber(String $ip) : Int
    {
        return ip2long($ip);
    }

    //--------------------------------------------------------------------------------------------------------
    // Number To IP v4
    //--------------------------------------------------------------------------------------------------------
    //
    // @param int $numberAddress
    //
    //--------------------------------------------------------------------------------------------------------
    public static function numberToIpv4(Int $numberAddress) : String
    {
        return long2ip($numberAddress);
    }

    //--------------------------------------------------------------------------------------------------------
    // Protected Clean HTTP
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $host
    //
    //--------------------------------------------------------------------------------------------------------
    protected static function cleanHttp($host)
    {
        return str_ireplace(['http://', 'https://'], '', $host);
    }
}
