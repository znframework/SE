interface CoalesceInterface
{
    //--------------------------------------------------------------------------------------------------
    // Null Coalesce
    //--------------------------------------------------------------------------------------------------
    //
    // @param var   &$var
    // @param mixed $value
    //
    // @return void
    //
    //--------------------------------------------------------------------------------------------------
    public static function null( & $var, $value);

    //--------------------------------------------------------------------------------------------------
    // False Coalesce
    //--------------------------------------------------------------------------------------------------
    //
    // @param var   &$var
    // @param mixed $value
    //
    // @return void
    //
    //--------------------------------------------------------------------------------------------------
    public static function false( & $var, $value);

    //--------------------------------------------------------------------------------------------------
    // Empty Coalesce
    //--------------------------------------------------------------------------------------------------
    //
    // @param var   &$var
    // @param mixed $value
    //
    // @return void
    //
    //--------------------------------------------------------------------------------------------------
    public static function empty( & $var, $value);
}
