    //--------------------------------------------------------------------------------------------------------
    // Url()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $url
    //
    //--------------------------------------------------------------------------------------------------------
    public function url(String $url) : MLGrid
    {
        \ML::url($url);

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // limit()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $limit
    //
    //--------------------------------------------------------------------------------------------------------
    public function limit(Int $limit) : MLGrid
    {
        \ML::limit($limit);

        return $this;
    }

    //--------------------------------------------------------------------------------------------------------
    // Create
    //--------------------------------------------------------------------------------------------------------
    //
    // @param mixed $app
    //
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function create($app = NULL) : String
    {
        return \ML::table($app);
    }
}
